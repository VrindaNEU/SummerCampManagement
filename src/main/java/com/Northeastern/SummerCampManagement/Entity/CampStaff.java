package com.Northeastern.SummerCampManagement.Entity;

import jakarta.persistence.Entity;
import java.time.LocalDate;

enum Designation {
CampLead,
CampManager,
CampAssistant
}
/**
 *
 * @author vrind
 */

@Entity
public class CampStaff extends AppUser{
    
    String firstName;
    String lastName;
    String email;
    String contactNumber;
    Designation designation;
    Boolean activeStatus;
    
    //Constructors

    public CampStaff() {
    }

    public CampStaff(Integer userId, String Username, String Password, Role role, LocalDate registrationDate) {
        super(userId, Username, Password, role, registrationDate);
    }
    
    
    
    
    //Getters and Setters

    public String getFirstName() {
        return firstName;
    }

    public void setFirstName(String firstName) {
        this.firstName = firstName;
    }

    public String getLastName() {
        return lastName;
    }

    public void setLastName(String lastName) {
        this.lastName = lastName;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getContactNumber() {
        return contactNumber;
    }

    public void setContactNumber(String contactNumber) {
        this.contactNumber = contactNumber;
    }

    public Designation getDesignation() {
        return designation;
    }

    public void setDesignation(Designation designation) {
        this.designation = designation;
    }

    public Boolean getActiveStatus() {
        return activeStatus;
    }

    public void setActiveStatus(Boolean activeStatus) {
        this.activeStatus = activeStatus;
    }

    public Integer getUserId() {
        return userId;
    }

    public void setUserId(Integer userId) {
        this.userId = userId;
    }

    public String getUsername() {
        return Username;
    }

    public void setUsername(String Username) {
        this.Username = Username;
    }

    public String getPassword() {
        return Password;
    }

    public void setPassword(String Password) {
        this.Password = Password;
    }

    public Role getRole() {
        return role;
    }

    public void setRole(Role role) {
        this.role = role;
    }

    public LocalDate getRegistrationDate() {
        return registrationDate;
    }

    public void setRegistrationDate(LocalDate registrationDate) {
        this.registrationDate = registrationDate;
    }
    
    
    
    
}

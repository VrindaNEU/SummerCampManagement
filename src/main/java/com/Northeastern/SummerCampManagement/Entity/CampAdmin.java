package com.Northeastern.SummerCampManagement.Entity;

import jakarta.persistence.Entity;
import java.time.LocalDate;

/**
 *
 * @author vrind
 */
@Entity
public class CampAdmin extends AppUser{
    
    String email;
    String contactNumber;

    //Constructors
    public CampAdmin() {
    }

    public CampAdmin(Integer userId, String Username, String Password, Role role, LocalDate registrationDate) {
        super(userId, Username, Password, role, registrationDate);
    }
    
    // Getters and Setters

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getPhoneNumber() {
        return contactNumber;
    }

    public void setPhoneNumber(String phoneNumber) {
        this.contactNumber = phoneNumber;
    }

    public Integer getUserId() {
        return userId;
    }

    public void setUserId(Integer userId) {
        this.userId = userId;
    }

    public String getUsername() {
        return Username;
    }

    public void setUsername(String Username) {
        this.Username = Username;
    }

    public String getPassword() {
        return Password;
    }

    public void setPassword(String Password) {
        this.Password = Password;
    }

    public Role getRole() {
        return role;
    }

    public void setRole(Role role) {
        this.role = role;
    }

    public LocalDate getRegistrationDate() {
        return registrationDate;
    }

    public void setRegistrationDate(LocalDate registrationDate) {
        this.registrationDate = registrationDate;
    }
    
    
    
}
